    public void Show(){
        DefaultTableModel tmodelEmployee = (DefaultTableModel) tableEmployee.getModel();
        String [] data = new String[6];
        for (int i = 0; i < Management.listEmployees.size(); i++) {
            data [0] = String.valueOf(Management.listEmployees.get(i).getCode());
            data [1] = Management.listEmployees.get(i).getName();
            data [2] = Management.listEmployees.get(i).getSurname();
            data [3] = String.valueOf(Management.listEmployees.get(i).getBirth());
            data [4] = String.valueOf(Management.listEmployees.get(i).getIncome());
            data [5] = String.valueOf(Management.listEmployees.get(i).getSalary());
            tmodelEmployee.addRow(data);
        }
    }
    
